SET NAMES utf8mb4;
SET CHARACTER SET utf8mb4;
CREATE TABLE IF NOT EXISTS produkt (
    id INT(11) NOT NULL AUTO_INCREMENT,
    nazwa VARCHAR(100) NOT NULL,
    kategoria VARCHAR(50) NOT NULL,
    cena DECIMAL(10,2) NOT NULL,
    stan_magazynowy INT(11) NOT NULL DEFAULT 0,
    producent VARCHAR(60) DEFAULT NULL,
    PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_polish_ci;
CREATE TABLE IF NOT EXISTS klient (
    id INT(11) NOT NULL AUTO_INCREMENT,
    imie VARCHAR(50) NOT NULL,
    nazwisko VARCHAR(50) NOT NULL,
    email VARCHAR(100) DEFAULT NULL,
    miasto VARCHAR(60) DEFAULT NULL,
    rok_rejestracji INT(4) DEFAULT NULL,
    PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_polish_ci;
CREATE TABLE IF NOT EXISTS zamowienie (
    id INT(11) NOT NULL AUTO_INCREMENT,
    id_produkt INT(11) NOT NULL,
    id_klient INT(11) NOT NULL,
    ilosc INT(11) NOT NULL DEFAULT 1,
    data_zamowienia DATE NOT NULL,
    status VARCHAR(30) DEFAULT 'nowe',
    PRIMARY KEY (id),
    FOREIGN KEY (id_produkt) REFERENCES produkt(id),
    FOREIGN KEY (id_klient) REFERENCES klient(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_polish_ci;
